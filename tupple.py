#Slicing Tuple

months =  ("jan","feb","mar","april","may","jun","july","aug","sep","oct","nov","Dec")
print("first 3 months", months[:3])
print("summer months",months[5:8])
print("winter months",months[-4:])
print("Every other months",months[::2])
print("months in reverse",months[::-1])

